package com.spring.biz.vo;

public class LikeVO {
	
    private int likeCode;
    
    private int itemCode;
    
    private String userId;
    
    private String likeState;

    private String itemName;
    
    private String itemPrice;
    
    private String itemDiscount;
    
    private String itemThumbnail;
    
    private String likeViewState;
    
    private int PageIndex;
    
    
    
	public String getLikeViewState() {
		return likeViewState;
	}
	public void setLikeViewState(String likeViewState) {
		this.likeViewState = likeViewState;
	}
	public int getLikeCode() {
		return likeCode;
	}
	public void setLikeCode(int likeCode) {
		this.likeCode = likeCode;
	}
	public int getItemCode() {
		return itemCode;
	}
	public void setItemCode(int itemCode) {
		this.itemCode = itemCode;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getLikeState() {
		return likeState;
	}
	public void setLikeState(String likeState) {
		this.likeState = likeState;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getItemPrice() {
		return itemPrice;
	}
	public void setItemPrice(String itemPrice) {
		this.itemPrice = itemPrice;
	}
	public String getItemDiscount() {
		return itemDiscount;
	}
	public void setItemDiscount(String itemDiscount) {
		this.itemDiscount = itemDiscount;
	}
	public String getItemThumbnail() {
		return itemThumbnail;
	}
	public void setItemThumbnail(String itemThumbnail) {
		this.itemThumbnail = itemThumbnail;
	}
	public int getPageIndex() {
		return PageIndex;
	}
	public void setPageIndex(int pageIndex) {
		PageIndex = pageIndex;
	}
	
	
}
