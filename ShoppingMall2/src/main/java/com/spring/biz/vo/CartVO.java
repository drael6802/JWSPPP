package com.spring.biz.vo;

import java.util.List;

public class CartVO {

    // 장바구니_코드 
    private int cartCode;

    // 유저_아이디 
    private String userId;

    // 아이템_코드 
	private int itemCode;

    // 아이템_수량 
    private int itemCount;

    // 아이텝_가격 
    private int itemPrice;

    // 구매여부 
    private String itemStatus;

    // 아이템_이름 
    private String itemName;

    // 옵션 
    private String options;

    // 아이템_이미지 썸네일 
    private String itemThumbnail;

    // 아이템_할인 
    private int itemDiscount;
    
    //아이템 재고
    private int itemInventory;
    
    private List<CartVO> cartVOList;
    
    private String cartCodeList;

	public int getCartCode() {
		return cartCode;
	}

	public void setCartCode(int cartCode) {
		this.cartCode = cartCode;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public int getItemCode() {
		return itemCode;
	}

	public void setItemCode(int itemCode) {
		this.itemCode = itemCode;
	}

	public int getItemCount() {
		return itemCount;
	}

	public void setItemCount(int itemCount) {
		this.itemCount = itemCount;
	}

	public int getItemPrice() {
		return itemPrice;
	}

	public void setItemPrice(int itemPrice) {
		this.itemPrice = itemPrice;
	}

	public String getItemStatus() {
		return itemStatus;
	}

	public void setItemStatus(String itemStatus) {
		this.itemStatus = itemStatus;
	}

	public String getItemName() {
		return itemName;
	}

	public void setItemName(String itemName) {
		this.itemName = itemName;
	}

	public String getOptions() {
		return options;
	}

	public void setOptions(String options) {
		this.options = options;
	}

	public String getItemThumbnail() {
		return itemThumbnail;
	}

	public void setItemThumbnail(String itemThumbnail) {
		this.itemThumbnail = itemThumbnail;
	}

	public int getItemDiscount() {
		return itemDiscount;
	}

	public void setItemDiscount(int itemDiscount) {
		this.itemDiscount = itemDiscount;
	}

	public List<CartVO> getCartVOList() {
		return cartVOList;
	}

	public void setCartVOList(List<CartVO> cartVOList) {
		this.cartVOList = cartVOList;
	}


	public String getCartCodeList() {
		return cartCodeList;
	}

	public void setCartCodeList(String cartCodeList) {
		this.cartCodeList = cartCodeList;
	}

	public int getItemInventory() {
		return itemInventory;
	}

	public void setItemInventory(int itemInventory) {
		this.itemInventory = itemInventory;
	}

	@Override
	public String toString() {
		return "CartVO [cartCode=" + cartCode + ", userId=" + userId + ", itemCode=" + itemCode + ", itemCount="
				+ itemCount + ", itemPrice=" + itemPrice + ", itemStatus=" + itemStatus + ", itemName=" + itemName
				+ ", options=" + options + ", itemThumbnail=" + itemThumbnail + ", itemDiscount=" + itemDiscount
				+ ", cartVOList=" + cartVOList + "]";
	}

 
}
